namespace Rift.Forms
{
    /// <summary>
    /// Specifies the custom buttons states.
    /// </summary>
    public enum ButtonState
    {
        /// <summary>
        /// Inactive button state.
        /// </summary>
        Inactive,
        /// <summary>
        /// Active button state.
        /// </summary>
        Active,
        /// <summary>
        /// The button is pressed.
        /// </summary>
        Pressed
    }
}